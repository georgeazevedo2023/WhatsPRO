interface PromptStudioProps {
  config: Record<string, any>;
  onChange: (updates: Record<string, any>) => void;
}

interface PromptSection {
  key: string;
  label: string;
  icon: any;
  description: string;
  examples: string;
  autoGenerated?: boolean;
}

